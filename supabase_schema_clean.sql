-- 1. BUSINESSES & TENANCY MODEL
CREATE TABLE businesses (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL,
    industry_code TEXT NOT NULL,
    license_key TEXT UNIQUE,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- 2. USER PROFILES & ACCOUNTS (Linked with auth.users)
CREATE TABLE profiles (
    id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    email TEXT UNIQUE NOT NULL,
    role TEXT DEFAULT 'admin' CHECK (role IN ('superadmin', 'admin', 'staff', 'customer')),
    business_id UUID REFERENCES businesses(id) ON DELETE SET NULL,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- 3. CENTRAL LICENSE REGISTRY (Activations engine)
CREATE TABLE license_codes (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    license_key TEXT UNIQUE NOT NULL,
    nganh_nghe TEXT NOT NULL,
    trang_thai TEXT DEFAULT 'Sẵn sàng' CHECK (trang_thai IN ('Sẵn sàng', 'Đã kích hoạt', 'Đã khóa')),
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- ==================================================================================
-- PART II: CORE BUSINESS OPERATIONS (RETAIL, F&B, SPA, NAILS, KARAOKE)
-- ==================================================================================

-- 4. STAFF & HUMAN RESOURCES (BIGINT PK for Flask high-fidelity compatibility)
CREATE TABLE staff (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    phone TEXT,
    role TEXT NOT NULL,
    commission_rate NUMERIC DEFAULT 0 CHECK (commission_rate >= 0 AND commission_rate <= 100),
    is_active BOOLEAN DEFAULT true,
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- 5. ATTENDANCE & TIMECARDS (Chấm công định vị & chuyên cần)
CREATE TABLE attendance (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    staff_id BIGINT REFERENCES staff(id) ON DELETE CASCADE,
    clock_in TIMESTAMPTZ NOT NULL,
    clock_out TIMESTAMPTZ,
    latitude_in NUMERIC,
    longitude_in NUMERIC,
    latitude_out NUMERIC,
    longitude_out NUMERIC,
    status TEXT DEFAULT 'Present' CHECK (status IN ('Present', 'Late', 'Absent', 'On Leave')),
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- 6. PAYROLL SYSTEM (Kết toán bảng lương tự động)
CREATE TABLE payroll (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    staff_id BIGINT REFERENCES staff(id) ON DELETE CASCADE,
    month_year TEXT NOT NULL, -- Format 'MM/YYYY'
    base_salary NUMERIC NOT NULL DEFAULT 0 CHECK (base_salary >= 0),
    allowance NUMERIC DEFAULT 0 CHECK (allowance >= 0),
    commission NUMERIC DEFAULT 0 CHECK (commission >= 0),
    deduction NUMERIC DEFAULT 0 CHECK (deduction >= 0),
    net_salary NUMERIC NOT NULL DEFAULT 0 CHECK (net_salary >= 0),
    status TEXT DEFAULT 'Unpaid' CHECK (status IN ('Unpaid', 'Paid', 'Pending')),
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- 7. SALARY CONFIGURATIONS (Cấu hình lương cơ bản nhân sự)
CREATE TABLE salary_configs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    staff_id BIGINT REFERENCES staff(id) ON DELETE CASCADE UNIQUE,
    base_salary NUMERIC NOT NULL DEFAULT 0 CHECK (base_salary >= 0),
    allowance NUMERIC DEFAULT 0 CHECK (allowance >= 0),
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- 8. CUSTOMERS DATABASE (Core CRM repository)
CREATE TABLE customers (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    phone TEXT,
    email TEXT,
    gender TEXT,
    dob DATE,
    tier TEXT DEFAULT 'Normal' CHECK (tier IN ('Normal', 'VIP', 'VVIP', 'Silver', 'Gold')),
    loyalty_points INTEGER DEFAULT 0 CHECK (loyalty_points >= 0),
    total_spent NUMERIC DEFAULT 0 CHECK (total_spent >= 0),
    join_date DATE DEFAULT CURRENT_DATE,
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- 9. PRODUCTS & SERVICES CATALOG (Dùng chung cho hàng hóa bán lẻ & F&B và các liệu trình Spa/Nails)
CREATE TABLE products (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    category TEXT,
    channel_type TEXT DEFAULT 'retail' CHECK (channel_type IN ('retail', 'spa', 'nail', 'fnb')),
    stock INTEGER DEFAULT 0,
    price NUMERIC NOT NULL CHECK (price >= 0),
    cost_price NUMERIC DEFAULT 0 CHECK (cost_price >= 0),
    image TEXT,
    is_active INTEGER DEFAULT 1 CHECK (is_active IN (0, 1)),
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- 10. DINING TABLES (Sơ đồ bàn phục vụ F&B / Karaoke)
CREATE TABLE dining_tables (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    status TEXT DEFAULT 'Còn trống' CHECK (status IN ('Còn trống', 'Đang phục vụ', 'Đã đặt trước')),
    qr_token TEXT UNIQUE NOT NULL,
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

-- 11. TABLE ORDERS (Giỏ hàng/Giao dịch treo tạm thời tại bàn POS)
CREATE TABLE table_orders (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    table_id BIGINT REFERENCES dining_tables(id) ON DELETE CASCADE,
    product_id BIGINT REFERENCES products(id) ON DELETE CASCADE,
    quantity INTEGER DEFAULT 1 CHECK (quantity > 0),
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- 12. TRANSACTIONS & BILLING (Hóa đơn hoàn tất)
CREATE TABLE orders (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_code TEXT UNIQUE NOT NULL,
    channel TEXT DEFAULT 'retail' CHECK (channel IN ('retail', 'fnb', 'spa', 'nail', 'karaoke')),
    total_amount NUMERIC NOT NULL CHECK (total_amount >= 0),
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- 13. ORDER ITEM ENTRIES (Chi tiết giao dịch hóa đơn)
CREATE TABLE order_items (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    order_id BIGINT REFERENCES orders(id) ON DELETE CASCADE,
    product_id BIGINT REFERENCES products(id) ON DELETE CASCADE,
    quantity INTEGER NOT NULL CHECK (quantity > 0),
    price NUMERIC NOT NULL CHECK (price >= 0),
    total_price NUMERIC NOT NULL CHECK (total_price >= 0),
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    created_at TIMESTAMPTZ DEFAULT now()
);

-- 14. BOOKING & SCHEDULING (Lịch hẹn dịch vụ Spa/Nail/Bàn tiệc)
CREATE TABLE appointments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_name TEXT NOT NULL,
    customer_phone TEXT NOT NULL,
    service_id BIGINT REFERENCES products(id) ON DELETE CASCADE,
    staff_id BIGINT REFERENCES staff(id) ON DELETE SET NULL,
