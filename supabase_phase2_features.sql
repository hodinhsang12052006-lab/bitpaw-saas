-- -------------------------------------------------------------------------
-- 1) restock_proposals — phiếu đề xuất nhập hàng do AI tạo (module Kho)
-- -------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.restock_proposals (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    business_id text NOT NULL,
    product_id BIGINT,
    product_name text,
    current_stock NUMERIC,
    avg_daily_sales NUMERIC,
    suggested_qty INTEGER,
    reason text,
    status text NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'approved', 'dismissed')),
    created_at TIMESTAMPTZ DEFAULT now()
);
ALTER TABLE public.restock_proposals ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Tenant isolation for restock_proposals" ON public.restock_proposals;
CREATE POLICY "Tenant isolation for restock_proposals" ON public.restock_proposals
    FOR ALL TO authenticated
    USING (business_id = (auth.jwt() ->> 'business_id') AND COALESCE(auth.jwt() ->> 'scope', 'staff') <> 'qr_public')
    WITH CHECK (business_id = (auth.jwt() ->> 'business_id') AND COALESCE(auth.jwt() ->> 'scope', 'staff') <> 'qr_public');

