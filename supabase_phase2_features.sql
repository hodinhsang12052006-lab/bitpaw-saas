-- -------------------------------------------------------------------------
-- 4) business_memberships — 1 chủ sở hữu (owner_user_id) có thể quản lý NHIỀU
--    business_id (chi nhánh). Bảng này KHÔNG scope theo business_id như các
--    bảng khác (chính nó là bảng định nghĩa quyền sở hữu), nên policy dựa
--    theo owner_user_id thay vì business_id.
-- -------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.business_memberships (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    owner_user_id text NOT NULL,
    business_id text NOT NULL,
    branch_name text,
    is_primary boolean DEFAULT false,
    created_at TIMESTAMPTZ DEFAULT now(),
    UNIQUE (owner_user_id, business_id)
);
ALTER TABLE public.business_memberships ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "Owner isolation for business_memberships" ON public.business_memberships;
CREATE POLICY "Owner isolation for business_memberships" ON public.business_memberships
    FOR ALL TO authenticated
