-- 3. CREATE & REPAIR MISSING PAYMENT TRANSACTIONS TABLE
-- ----------------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS payment_transactions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    amount NUMERIC NOT NULL CHECK (amount >= 0),
    status TEXT DEFAULT 'pending' CHECK (status IN ('pending', 'completed', 'failed', 'refunded')),
    business_id UUID REFERENCES businesses(id) ON DELETE CASCADE,
    created_at TIMESTAMPTZ DEFAULT now(),
    updated_at TIMESTAMPTZ DEFAULT now()
);

ALTER TABLE payment_transactions ENABLE ROW LEVEL SECURITY;

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_policies WHERE tablename = 'payment_transactions' AND policyname = 'Allow all access to payment_transactions for tenant'
    ) THEN
        CREATE POLICY "Allow all access to payment_transactions for tenant" ON payment_transactions FOR ALL TO anon, authenticated USING (true) WITH CHECK (true);
    END IF;
